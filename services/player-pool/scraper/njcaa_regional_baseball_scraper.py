"""
NJCAA Regional Baseball + Softball Scraper
Scrapes per-team batting stats from all 6 NJCAA regional sites.

Covers both sports — identical 19-column format:
  # | Name | Yr | Pos | g | ab | r | h | 2b | 3b | hr | rbi | bb | k | sb | cs | avg | obp | slg

Regions:
  r2  : region2athletics.com       (sball: 16, bsb: 19)
  r5  : njcaaregion5.com           (sball: 17, bsb: 26)
  r14 : njcaaregion14.com          (sball: 15, bsb: 13)
  r15 : region15athletics.com      (sball:  5, bsb: 13)
  r19 : njcaaregion19.com          (sball: 18, bsb: 20)
  r24 : njcaaregion24.com          (sball: 19, bsb: 20)
Total: sball=90, bsb=111

Team slugs loaded from njcaa_regional_baseball_teams.json (generated by slug-discovery agent).
If the JSON doesn't exist or is missing a region, falls back to dynamic listing-page discovery.

Usage:
  python3 njcaa_regional_baseball_scraper.py --sport sball
  python3 njcaa_regional_baseball_scraper.py --sport bsb
  python3 njcaa_regional_baseball_scraper.py            # both sports
  python3 njcaa_regional_baseball_scraper.py --region r5 --sport sball
"""
