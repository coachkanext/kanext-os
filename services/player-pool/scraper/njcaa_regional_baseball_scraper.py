def fetch(url: str, base: str, referer: str, retries: int = 3) -> httpx.Response | None:
    hdrs = {**BROWSER_HEADERS, "Origin": base, "Referer": referer}
    for attempt in range(retries):
        try:
            r = httpx.get(url, headers=hdrs, timeout=20, follow_redirects=True)
            if r.status_code == 202 and len(r.content) < 5000:
                wait = 90 if attempt == 0 else 120
                print(f"    [WAF] waiting {wait}s...")
                time.sleep(wait)
                continue
            return r
        except Exception as e:
            print(f"    [err] {url}: {e}")
            time.sleep(10)
    return None


def discover_slugs(base: str, sport: str, season: str) -> list[str]:
    """Try to fetch team listing page and extract slugs. Returns [] on WAF block."""
    list_url = f"{base}/sports/{sport}/{season}/teams"
    nav_hdrs = {
        "User-Agent": BROWSER_HEADERS["User-Agent"],
        "Accept": "text/html,application/xhtml+xml,*/*;q=0.8",
        "Accept-Language": "en-US,en;q=0.9",
        "Sec-Fetch-Dest": "document",
        "Sec-Fetch-Mode": "navigate",
        "Sec-Fetch-Site": "none",
    }
    try:
        r = httpx.get(list_url, headers=nav_hdrs, timeout=20, follow_redirects=True)
        if r.status_code == 202 and len(r.content) < 5000:
            return []
        if r.status_code != 200:
            return []
        soup = BeautifulSoup(r.text, "html.parser")
        links = [
            a["href"] for a in soup.find_all("a", href=True)
            if f"/sports/{sport}/{season}/teams/" in a["href"]
            and not a["href"].rstrip("/").endswith("/teams")
        ]
        return list(dict.fromkeys(
            h.split("/teams/")[1].split("?")[0].split("/")[0]
            for h in links
            if h.split("/teams/")[1].split("?")[0].split("/")[0]
        ))
    except Exception:
        return []


# ── Parsing ───────────────────────────────────────────────────────────────────

def _dash_or_int(v: str | None) -> int | None:
    if not v or v.strip() in ("-", "", "—"):
        return None
    try:
        return int(str(v).replace(",", "").strip())
    except ValueError:
        return None


def _stat_pct(v: str | None) -> float | None:
    """Parse batting avg like '.395' or '0.395' → stored as 0-1."""
    if not v or v.strip() in ("-", "", "—"):
        return None
    try:
        return round(float(str(v).strip()), 4)
    except ValueError:
        return None


def parse_batting_table(html: str) -> list[dict]:
    """
    Parse PrestoSports baseball/softball batting table.

    Fixed column layout (confirmed across all sports/categories):
      0:# 1:Name 2:Yr 3:Pos 4:g 5:ab 6:r 7:h 8:2b 9:3b
      10:hr 11:rbi 12:bb 13:k 14:sb 15:cs 16:avg 17:obp 18:slg
    """
    soup = BeautifulSoup(html, "html.parser")
    table = soup.find("table")
    if not table:
        return []

    thead = table.find("thead")
    if not thead:
        return []

    # Detect column positions from headers (lowercase)
    raw_hdrs = [th.get_text(strip=True).lower() for th in thead.find_all(["th", "td"])]

    def ci(names: list[str], default: int) -> int:
        for n in names:
            for i, h in enumerate(raw_hdrs):
                if h == n:
                    return i
        return default

    C_JERSEY = ci(["#", "no", "num"], 0)
    C_NAME   = ci(["name", "player"], 1)
    C_YR     = ci(["yr", "cl", "class", "year"], 2)
    C_POS    = ci(["pos", "position"], 3)
    C_G      = ci(["g", "gp"], 4)
    C_AB     = ci(["ab"], 5)
    C_R      = ci(["r"], 6)
    C_H      = ci(["h"], 7)
    C_2B     = ci(["2b"], 8)
    C_3B     = ci(["3b"], 9)
    C_HR     = ci(["hr"], 10)
    C_RBI    = ci(["rbi"], 11)
    C_BB     = ci(["bb"], 12)
    C_K      = ci(["k", "so"], 13)
    C_SB     = ci(["sb"], 14)
    C_CS     = ci(["cs"], 15)
    C_AVG    = ci(["avg", "ba"], 16)
    C_OBP    = ci(["obp"], 17)
    C_SLG    = ci(["slg"], 18)

    rows = []
    tbody = table.find("tbody") or table
    for tr in tbody.find_all("tr"):
        cells = tr.find_all(["td", "th"])
        if len(cells) < 10:
            continue

        def cell(i: int) -> str:
            return " ".join(cells[i].get_text(strip=True).split()) if i < len(cells) else ""

        name_raw = cell(C_NAME)
        if not name_raw or name_raw.lower() in (
            "name", "player", "totals", "total", "", "opponent", "opponents",
            "home", "away", "exhibition", "conference", "non-conference"
        ):
            continue
        name = " ".join(name_raw.split())

        avg = _stat_pct(cell(C_AVG))
        obp = _stat_pct(cell(C_OBP))
        slg = _stat_pct(cell(C_SLG))
        ops = round(obp + slg, 4) if obp is not None and slg is not None else None

        rows.append({
            "name":    name,
            "jersey":  cell(C_JERSEY) or None,
            "pos":     cell(C_POS) or None,
            "cl":      cell(C_YR) or None,
            "g":       _dash_or_int(cell(C_G)),
            "ab":      _dash_or_int(cell(C_AB)),
            "r":       _dash_or_int(cell(C_R)),
            "h":       _dash_or_int(cell(C_H)),
            "doubles": _dash_or_int(cell(C_2B)),
            "triples": _dash_or_int(cell(C_3B)),
            "hr":      _dash_or_int(cell(C_HR)),
            "rbi":     _dash_or_int(cell(C_RBI)),
            "bb":      _dash_or_int(cell(C_BB)),
            "k":       _dash_or_int(cell(C_K)),
            "sb":      _dash_or_int(cell(C_SB)),
            "cs":      _dash_or_int(cell(C_CS)),
            "avg":     avg,
            "obp":     obp,
            "slg":     slg,
            "ops":     ops,
        })
    return rows


def parse_fullpage_baseball_stats(html: str) -> list[dict]:
    """
    Parse PrestoSports FULLPAGE baseball/softball batting stats (r22 / acccathletics.com).
    Stats embedded in full team page — no AJAX endpoint.
    Finds the season batting table by column signature (has g/ab/h/avg, NOT avg/g or h/g).
    """
    soup = BeautifulSoup(html, "html.parser")
    target_table = None
    header_row_idx = 0
    for t in soup.find_all("table"):
        rows = t.find_all("tr")
        for ri, tr in enumerate(rows[:3]):
            hdrs = [td.get_text(strip=True).lower() for td in tr.find_all(["th", "td"])]
            has_stats = ("g" in hdrs or "gp" in hdrs) and "ab" in hdrs and "h" in hdrs and "avg" in hdrs
            not_per_game = "avg/g" not in hdrs and "h/g" not in hdrs and "ab/g" not in hdrs
            has_name = "name" in hdrs or "player" in hdrs
            if has_stats and not_per_game and has_name:
                target_table = t
                header_row_idx = ri
                break
        if target_table:
            break
    if not target_table:
        return []
    rows = target_table.find_all("tr")
    header_html = str(rows[header_row_idx])
    body_html = "".join(str(tr) for tr in rows[header_row_idx + 1:])
    synthetic = f"<table><thead><tr>{header_html}</tr></thead><tbody>{body_html}</tbody></table>"
    return parse_batting_table(synthetic)


def parse_pitching_table(html: str) -> list[dict]:
    """
    Parse PrestoSports baseball pitching table from AJAX endpoint:
      ?tmpl=brief-category-template&pos=p&r=0

    Columns vary by region but always include:
      # | Name | Yr | Pos | app | gs | w | l | sv | cg | ip | h | r | er | bb | k | era | whip
    Some regions add: k/9 | hr | wp | hbp
    """
    soup = BeautifulSoup(html, "html.parser")
    table = soup.find("table")
    if not table:
        return []
    thead = table.find("thead")
    if not thead:
        return []

    raw_hdrs = [th.get_text(strip=True).lower() for th in thead.find_all(["th", "td"])]

    def ci(names: list[str], default: int) -> int:
        for n in names:
            for i, h in enumerate(raw_hdrs):
                if h == n:
                    return i
        return default

    C_JERSEY = ci(["#", "no", "num"], 0)
    C_NAME   = ci(["name", "player"], 1)
    C_YR     = ci(["yr", "cl", "class", "year"], 2)
    C_POS    = ci(["pos", "position"], 3)
    C_APP    = ci(["app", "g", "gp"], 4)
    C_GS     = ci(["gs"], 5)
    C_W      = ci(["w"], 6)
    C_L      = ci(["l"], 7)
    C_SV     = ci(["sv"], 8)
    C_CG     = ci(["cg"], 9)
    C_IP     = ci(["ip"], 10)
    C_H      = ci(["h"], 11)
    C_R      = ci(["r"], 12)
    C_ER     = ci(["er"], 13)
    C_BB     = ci(["bb"], 14)
    C_K      = ci(["k", "so"], 15)
    C_HR     = ci(["hr"], 17)
    C_ERA    = ci(["era"], 18)
    C_WHIP   = ci(["whip"], 19)
    C_WP     = ci(["wp"], -1)
    C_HBP    = ci(["hbp"], -1)

    def _int(v: str | None) -> int | None:
        if not v or v.strip() in ("-", "", "—"):
            return None
        try:
            return int(str(v).replace(",", "").strip())
        except ValueError:
            return None

    def _flt(v: str | None, ndigits: int = 2) -> float | None:
        if not v or v.strip() in ("-", "", "—", "∞", "inf", "INF", "infinity"):
            return None
        try:
            f = float(str(v).strip())
            if f != f or f == float("inf") or f == float("-inf"):  # NaN or inf
                return None
            return round(f, ndigits)
        except ValueError:
            return None

    def _ip(v: str | None) -> float | None:
        """Parse innings pitched: '80.0' or '80.1' stored as decimal."""
        return _flt(v, 1)

    rows = []
    tbody = table.find("tbody") or table
    for tr in tbody.find_all("tr"):
        cells = tr.find_all(["td", "th"])
        if len(cells) < 8:
            continue

        def cell(i: int) -> str:
            if i < 0 or i >= len(cells):
                return ""
            return " ".join(cells[i].get_text(strip=True).split())

        name_raw = cell(C_NAME)
        if not name_raw or name_raw.lower() in (
            "name", "player", "totals", "total", "", "opponent", "opponents",
        ):
            continue
        name = " ".join(name_raw.split())

        rows.append({
            "name":   name,
            "jersey": cell(C_JERSEY) or None,
            "pos":    cell(C_POS) or None,
            "cl":     cell(C_YR) or None,
            "app":    _int(cell(C_APP)),
            "gs":     _int(cell(C_GS)),
            "w":      _int(cell(C_W)),
            "l":      _int(cell(C_L)),
            "sv":     _int(cell(C_SV)),
            "cg":     _int(cell(C_CG)),
            "ip":     _ip(cell(C_IP)),
            "h":      _int(cell(C_H)),
            "r":      _int(cell(C_R)),
            "er":     _int(cell(C_ER)),
            "bb":     _int(cell(C_BB)),
            "so":     _int(cell(C_K)),
            "hr":     _int(cell(C_HR)),
            "era":    _flt(cell(C_ERA), 2),
            "whip":   _flt(cell(C_WHIP), 2),
            "wp":     _int(cell(C_WP)) if C_WP >= 0 else None,
            "hbp":    _int(cell(C_HBP)) if C_HBP >= 0 else None,
        })
    return rows


