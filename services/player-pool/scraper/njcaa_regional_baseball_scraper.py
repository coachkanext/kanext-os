def upsert_team(conn, region: str, sport: str, slug: str, base: str) -> int:
    row = conn.execute("""
        INSERT INTO njcaa_reg_baseball_teams (region, sport, slug, base_url, season)
        VALUES (%s, %s, %s, %s, %s)
        ON CONFLICT (region, sport, slug, season) DO UPDATE SET base_url = EXCLUDED.base_url
        RETURNING id
    """, (region, sport, slug, base, SEASONS[sport])).fetchone()
    return row["id"]


def upsert_player(conn, team_id: int, row: dict) -> int:
    r = conn.execute("""
        INSERT INTO njcaa_reg_baseball_players (team_id, jersey, full_name, position, class_year)
        VALUES (%s, %s, %s, %s, %s)
        ON CONFLICT (team_id, full_name) DO UPDATE SET
            jersey     = COALESCE(EXCLUDED.jersey,     njcaa_reg_baseball_players.jersey),
            position   = COALESCE(EXCLUDED.position,   njcaa_reg_baseball_players.position),
            class_year = COALESCE(EXCLUDED.class_year, njcaa_reg_baseball_players.class_year)
        RETURNING id
    """, (team_id, row.get("jersey"), row["name"], row.get("pos"), row.get("cl"))).fetchone()
    return r["id"]


